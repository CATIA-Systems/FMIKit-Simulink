/* Most FMI environments pass in calloc, but zero initialization was
   not guaranteed in early versions of FMI */
void* allocateMemory0(size_t nobj, size_t size) {

	void* obj;
	if (allocateMemory == NULL) {
		return NULL;
	} else {
		obj = allocateMemory(nobj, size);
		memset(obj, 0, nobj*size);
		return obj;
	}
}

static void logger(fmiComponent c, fmiString instanceName, fmiStatus status,
				   fmiString category, fmiString message, ...)
{
	char buf[4096];
	va_list ap;
    int capacity;
	Model* model;

	if (c==NULL) return;

	model = (Model*) c;
	if (model->loggingOn == fmiFalse && (status == fmiOK || status == fmiDiscard)) {
		return;
	}

	va_start(ap, message);
    capacity = sizeof(buf) - 1;
#if defined(_MSC_VER) && _MSC_VER>=1400
	vsnprintf_s(buf, capacity, _TRUNCATE, message, ap);
#else
    buf[capacity]=0;
	vsnprintf(buf, capacity, message, ap);
#endif
	va_end(ap);
	model->functions.logger(c, instanceName, status, category, buf);
}

static fmiString strDup(fmiCallbackFunctions *functions, fmiString s)
{
	const fmiString nullString = "<NULL>";
	const int maxLen = 1024;
	fmiString pos = s;
	int len;
	char* newS;

	if (s == NULL) {
		s = nullString;
	}
	len = (int) strlen(s);
	if (len > maxLen) {
		len = maxLen;
	}

	newS = (char*) allocateMemory0(len + 1, sizeof(char));
	if (newS == NULL) {
		return NULL;
	}
	strncpy(newS, s, len);
	return newS;
}


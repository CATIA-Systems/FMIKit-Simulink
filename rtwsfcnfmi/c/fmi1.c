/* Model status */
typedef enum {
	modelStatusInstantiated,
	modelStatusInitialized,
	modelStatusTerminated
} ModelStatus;

/* Model data structure */
typedef struct {
	fmiString instanceName;
	fmiBoolean loggingOn;
	SimStruct* S;
	real_T* dX;
	real_T* oldZC;
	int_T* numSampleHits;
	int_T fixed_in_minor_step_offset_tid;
	real_T nextHit_tid0;
	void** inputs;
	void** outputs;
	void** parameters;
	void** blockoutputs;
	void** dwork;
	fmiCallbackFunctions functions;
	fmiReal lastGetTime;
	fmiBoolean shouldRecompute;
	fmiBoolean isCoSim;
	fmiBoolean isDiscrete;
	fmiReal time;
	fmiReal nbrSolverSteps;
	fmiEventInfo eventInfo;
	ModelStatus status;
#if defined(_MSC_VER)
	HINSTANCE* mexHandles;
#else
	void** mexHandles;
#endif
	fmiReal* inputDerivatives;
	fmiReal derivativeTime;
} Model;



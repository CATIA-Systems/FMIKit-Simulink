static void extrapolateInputs(Model* model, fmiReal t)
{
	size_t i;
	fmiReal dt = (t - model->derivativeTime);
	for (i = 0; i < SFCN_FMI_NBR_INPUTS; i++) {
		if (model->inputDerivatives[i] != 0.0) {
			*((real_T*)(model->inputs[i])) += model->inputDerivatives[i] * dt;
#if defined(SFCN_FMI_VERBOSITY)
			logger(model, model->instanceName, fmiOK, "", "Extrapolated input #%d to value = %.16f\n", i, *((real_T*)(model->inputs[i])));
#endif
		}
	}
	model->derivativeTime = t;
}

fmiStatus fmiDoStep_(fmiComponent c, fmiReal currentCommunicationPoint, fmiReal communicationStepSize, fmiBoolean newStep)
{
	Model* model = (Model*) c;
	fmiStatus status = fmiOK;
	fmiReal lastSolverTime, nextSolverTime;
	fmiReal endStepTime;

	if (model->status == modelStatusInstantiated) {
        logger(model, model->instanceName, fmiWarning, "", "fmiDoStep: Slave is not initialized\n");
		return fmiWarning;
	}
	if (model->status == modelStatusTerminated) {
        logger(model, model->instanceName, fmiWarning, "", "fmiDoStep: Slave terminated in previous step\n");
		return fmiWarning;
	}
	if (!isEqual(model->time, currentCommunicationPoint)) {
		logger(model, model->instanceName, fmiWarning, "", "fmiDoStep: Invalid currentCommunicationPoint = %.16f, expected %.16f\n", currentCommunicationPoint, model->time);
		return fmiWarning;
	}
	model->S->mdlInfo->simTimeStep = MAJOR_TIME_STEP;
	if (fabs(communicationStepSize) < SFCN_FMI_EPS) {
		/* Zero step size; External event iteration, just recompute outputs */
		sfcnOutputs(model->S, 0);
		return fmiOK;
	}
	endStepTime = currentCommunicationPoint + communicationStepSize;
	lastSolverTime = model->nbrSolverSteps*SFCN_FMI_FIXED_STEP_SIZE;
	nextSolverTime = (model->nbrSolverSteps+1.0)*SFCN_FMI_FIXED_STEP_SIZE;
	while ( (nextSolverTime < endStepTime) || isEqual(nextSolverTime, endStepTime) ) {
#if defined(SFCN_FMI_VERBOSITY)
		logger(model, model->instanceName, fmiOK, "", "fmiDoStep: Internal solver step from %.16f to %.16f\n", lastSolverTime, nextSolverTime);
#endif
		/* Set time for state update */
		fmiSetTime_(c, lastSolverTime);
		/* Update continuous-time states */
		if (ssGetNumContStates(model->S) > 0) {
#if defined(SFCN_FMI_VERBOSITY)
			logger(model, model->instanceName, fmiOK, "", "fmiDoStep: Updating continuous states at time = %.16f.\n", model->S->mdlInfo->t[0]);
#endif
			/* Set ODE solver stop time */
			_ssSetSolverStopTime(model->S, nextSolverTime);
			/* Update states */
			rt_UpdateContinuousStates(model->S);
		}
		/* Set time for output calculations */
		fmiSetTime_(c, nextSolverTime);
		/* Extrapolate inputs */
		extrapolateInputs(model, nextSolverTime);
		/* Set sample hits and call mdlOutputs / mdlUpdate (always a discrete sample time = Fixed-step size) */
		fmiEventUpdate_(c, fmiFalse, &(model->eventInfo));
		/* Update solver times */
		lastSolverTime = nextSolverTime;
		model->nbrSolverSteps++;
		nextSolverTime = (model->nbrSolverSteps+1.0)*SFCN_FMI_FIXED_STEP_SIZE;
	}
	model->time = endStepTime;

	return fmiOK;
}


#define FMI_SET(label)												\
	Model* model = (Model*) c;												\
	size_t i;																\
    fmiBoolean allowed = fmiTrue;											\
	fmiBoolean paramChanged = fmiFalse;											\
																			\
	for (i = 0; i < nvr; i++) {												\
		const fmiValueReference r = vr[i];									\
		int index    = SFCN_FMI_INDEX(r);									\
		int dataType = SFCN_FMI_DATATYPE(r);								\
																			\
		switch (SFCN_FMI_CATEGORY(r)) {										\
        case SFCN_FMI_INPUT:												\
			setValue ## label(model->inputs[index], dataType, value[i]);	\
			model->shouldRecompute = fmiTrue;\
			break;															\
		case SFCN_FMI_STATE:												\
			if (model->isCoSim && model->status != modelStatusInstantiated) {\
				allowed = fmiFalse;	\
			} else {\
				ssGetContStates(model->S)[index] = (real_T)value[i];			\
				model->shouldRecompute = fmiTrue;\
			}\
			break;															\
		case SFCN_FMI_DERIVATIVE:\
			if (model->status == modelStatusInstantiated) {\
				ssGetdX(model->S)[index] = (real_T)value[i];\
			} else {\
				allowed = fmiFalse;			\
			}\
			break;															\
        case SFCN_FMI_BLOCKIO:												\
		    if (model->status == modelStatusInstantiated) {					\
				setValue ## label(model->blockoutputs[index], dataType, value[i]);	\
			} else {														\
                allowed = fmiFalse;											\
			}																\
			break;															\
		case SFCN_FMI_DWORK:												\
		    if (model->status == modelStatusInstantiated) {					\
			    setValue ## label(model->dwork[index], dataType, value[i]);	\
			} else {														\
                allowed = fmiFalse;											\
			}																\
			break;															\
		case SFCN_FMI_PARAMETER:											\
			if (model->status == modelStatusInstantiated) {					\
     			setValue ## label(model->parameters[index], dataType, value[i]);	\
				paramChanged = fmiTrue;\
			} else {														\
				allowed = fmiFalse;											\
			}																\
			break;															\
        case SFCN_FMI_OUTPUT:												\
     		if (model->status == modelStatusInstantiated) {					\
			    setValue ## label(model->outputs[index], dataType, value[i]);	\
			} else {														\
                allowed = fmiFalse;											\
			}																\
			break;															\
		default:															\
	        logger(model, model->instanceName, fmiWarning, "", "fmiSet"#label": cannot set %u\n", r);		\
            return fmiWarning;												\
		}																	\
		if (allowed == fmiFalse) {											\
			logger(model, model->instanceName, fmiWarning, "", "fmiSet"#label": may not change %u at this stage\n", r);	\
			return fmiWarning;												\
		}																	\
	}																		\
	if (paramChanged == fmiTrue && SFCN_FMI_LOAD_MEX) {											\
		sfcn_fmi_copyToSFcnParams_(model->S);\
		sfcn_fmi_mxGlobalTunable_(model->S, 1, 1);\
	}\
	return fmiOK


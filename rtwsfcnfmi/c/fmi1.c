	m->dX						= S->states.dX;
	S->states.contStateDisabled = (boolean_T*) allocateMemory0(S->sizes.numContStates+1, sizeof(boolean_T));
	S->states.discStates        = (real_T*) allocateMemory0(S->sizes.numDiscStates+1, sizeof(real_T));
